!----------------------------------------------------------------------
!2025-04-25　11:25
! プログラム名: exercise1
! 概要        : 駐車料金を計算するプログラム
! 入力        : 駐車時間 (x) - 実数値
! 出力        : 駐車料金 (i) - 整数値 (円)
! 処理の流れ  :
!   1. ユーザーに駐車時間を入力させる。
!   2. 入力された時間を24時間単位で日数 (d) と時間 (h) に分割。
!   3. 日数に基づく料金 (y1) を計算。
!   4. 時間に基づく料金 (y2) を計算。
!      - 6時間を超える場合は一律1800円。
!      - 6時間以下の場合は30分ごとに150円。
!   5. 合計料金 (y1 + y2) を整数値に丸めて出力。
! 注意事項    :
!   - 入力は24時間を超える場合も対応。
!   - 時間の計算は30分単位で切り捨て。
!   - 出力は日本円 (Yen) で表示。
!---------------------------------------------------------------------- 
program exercise1
    !フォーマット222は,小数を右揃え5桁で小数点以下1桁表示,1マス空けて,文字列を表示,1マス空けて,小数を右揃え5桁で小数点以下1桁表示,1マス空けて,文字列を表示
    !フォーマット223は,文字列を表示,整数を右揃え5桁で表示,1マス空けて,文字列を表示
    222 format(f5.1,1x,A,1x,f5.1,1x,A)
    223 format(A,i5,1x,A)
    implicit double precision (a-h,o-z)
    !write文でプログラムの概要を説明
    write(*,*)'This program calculates parking fee.'
    write(*,*)'How many hours do you park the car?'
    !x:駐車時間[hour]を入力
    read(*,*)x
    !x:駐車時間[h]を24時間単位で日数[d]と時間[h]に分割
    !h:時間[h]を24時間単位で分割
    if(x .le. 24)then
        h = x
        d = 0.d0
    else
        h = mod(x,24.d0)
        d = (x-h)/24.d0
    end if
    !dとhを表示
    write(*,222)d,'days and',h,'hours,right?'
    !y1:日数[d]に基づく料金を計算
    y1 = 1800*d
    !y2:時間[h]に基づく料金を計算
    !6時間を超える場合は一律1800円
    !6時間以下の場合は30分ごとに150円
    if(h .ge. 6.0d0)then
        y2=1800
    else
        !h:時間[h]を30分単位で切り捨て
        y2 = 150 * floor(h / 0.5d0)
    end if

    !i:合計料金を整数値に丸めて出力
    i=nint(y1+y2)

    write(*,223)'The parking fee is',i,'Yen.'


end program exercise1
