!***********************************************************************
! プログラム名: example4
! 概要:
!   このプログラムは、入力ファイル(input.dat)から行列とベクトルを読み込み、
!   行列とベクトルの積を計算し、その結果を出力ファイル(output.dat)に書き込む。
!
! 入力:
!   - input.dat:
!       1行目: 80文字以内の文字列 (スキップされる)
!       2行目: 行列のサイズ (nx)
!       3行目: 80文字以内の文字列 (スキップされる)
!       4行目以降: nx×nxの行列要素
!       (4+nx)行目: 80文字以内の文字列 (スキップされる)
!       (5+nx)行目以降: ベクトル要素
!
! 出力:
!   - output.dat:
!       計算結果のベクトルを有効数字5桁の倍精度小数形式で出力。
!
! 変数:
!   - md1: 行列やベクトルの最大サイズ (100に固定)
!   - nx: 実際の行列やベクトルのサイズ (入力ファイルから読み込み)
!   - aa: nx×nxの行列
!   - bb: nx次元のベクトル
!   - cc: 計算結果を格納するnx次元のベクトル
!   - ww: 計算用の一時変数
!
! 処理の流れ:
!   1. 入力ファイル(input.dat)を開く。
!   2. 行列サイズ(nx)を読み込む。
!   3. 行列(aa)とベクトル(bb)を読み込む。
!   4. 行列とベクトルの積を計算し、結果をベクトル(cc)に格納する。
!   5. 計算結果を出力ファイル(output.dat)に書き込む。
!
! 注意事項:
!   - 入力ファイルの形式が正しいことを前提としている。
!   - 行列サイズ(nx)は100以下である必要がある。
!***********************************************************************
program example4
!このプログラムでは，md1は100と読み替える．
    parameter(md1=100)
!aからh,oからzまでの頭文字をもつ変数を倍精度小数に型付け
    implicit double precision (a-h,o-z)
!配列の宣言
!aaは100行100列の行列，bbは100行のベクトル，ccは100行のベクトルを収容可
    dimension aa(md1,md1),bb(md1),cc(md1)


!10はinput.datに対応する．
    open(10,file='input.dat')
!11はoutput.datに対応する．
    open(11,file='output.dat')
!1行目は80文字以内の文字列を含んでいるのでスキップ
    read(10,'(a80)')
!2行目の行列のサイズを読み込んで変数nxに格納する．
    read(10,*) nx
!3行目は文字列を含んでいるのでスキップ
    read(10,'(a80)')
!4行目以降のnx*nx行列の要素を2重ループ構造で読み込む．
!aa(i,j)はi行j列の要素を表す．
!jを1からnxまで，iを1からnxまでループさせる．
    read(10,*) ((aa(i,j),j=1,nx),i=1,nx)
!4+nx行目は文字列を含んでいるのでスキップ
    read(10,'(a80)')
!5+nx行目以降のベクトルを読み込む．
!bb(i)はi行の要素を表す．
!iを1からnxまでループさせる．
    read(10,*) (bb(i),i=1,nx)


    do i = 1,nx 
        !計算用の変数wwを初期化する．
        ww = 0.d0
        do j = 1,nx
            !aa(i,j)とbb(j)の積を加算する．
            ww = ww + aa(i,j)*bb(j)
        end do
        !計算結果をcc(i)に格納する．
        cc(i) = ww
    end do

!計算結果をoutput.datに書き込む．
    write(11,*)'Computational result'
    do i=1,nx
        !cc(i)の値を出力する．
        !222の書式で出力する．
        write(11,222)cc(i)
    end do
!222は有効数字が5桁の倍精度小数を出力する書式を指定している．
222 format(f10.5)

end program example4